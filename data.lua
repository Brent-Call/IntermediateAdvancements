--Key design principles of this mod:
--1) Complexity should be optimal.
--	The most efficienct recipes for something should be more complex & more difficult to set up.
--2) Any added complexity should be optional.
--	If the player doesn't want to deal with the complexity, they should just be able to use their
--	builds from vanilla Factorio, & they should *just work* with no need for anything extra.
--	If the player wants to engage with complexity, they can, & they should be rewarded for it, but
--	there should not be a punishment for ignoring it.  For some players, Factorio is hard enough.